    private void btnGenerateTExtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGenerateTExtMouseClicked
        // TODO add your handling code here:
          try {
            // TODO add your handling code here:
            String LevelReq;
            LevelReq = LevelReqA.getSelectedItem().toString()
                    + "/" +
                    LevelReqT.getSelectedItem()
                    +"/" + LevelReqB.getSelectedItem().toString() ;
            
            String FileName = "C:/UWOTextFiles/Skills/" +txtSkillName.getText()+".txt";
            PrintWriter writer;
            writer = new PrintWriter(FileName, "UTF-8");
            
            writer.println("{{SkillInfo|shownfo={{{1}}}|imgsz={{{2|}}}");
            writer.println("|skill = " + txtSkillName.getText());
            writer.println("|cost= " + txtCost.getText());
            writer.println("|desc= " + txtDescription.getText());
            writer.println("|vigour=  " + txtvigour.getText());
            writer.println("|maxrank= " + cboxMaxRank.getSelectedItem().toString());
            writer.println("|lvreq= " + LevelReq );
            writer.println("|txtSkillReq = " + txtSkillReq.getText());
            writer.println("|addnfo= " + txtAdditionalInformation.getText());
            writer.println("}}") ;
            writer.println("<noinclude>");
            writer.println("==Training==");
            writer.println("{{NPCInfo/Table/Start}}");
             
            int i= 0;
            if (Trainers.size() != 0)
            {
             do
            {
               writer.println("{{:"+Trainers.get(i)+"}}");
               i++;
            }while(i < Trainers.size()  );
            }
           writer.println("|}");
           writer.println("==Aides and Duties==");
           writer.println("{| class = AidesTable");
           writer.println("!Aide !!Requirements");
          
          int j= 0;
            if (Aides.size() != 0)
            {
             do
            {
               writer.println("{{:"+Aides.get(j)+"|:{{PAGENAME}}}}");
               
               
               j++;
            }while(j < Aides.size()  );
            } 
           writer.println("|-");
           writer.println("|");
           writer.println("|}");
           writer.println("{{:Jobs}}");
           writer.println("</noinclude>");
            writer.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Skills.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(Skills.class.getName()).log(Level.SEVERE, null, ex);
        }
                
        
    }//GEN-LAST:event_btnGenerateTExtMouseClicked

